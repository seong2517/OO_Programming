using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace CSharpStudy
{
  class Jeong
  {
    public string name = "정성호";
  }

  class Student : Jeong
  {
    public void Name()
    {
      Console.WriteLine("학생의 이름은 " + name + "입니다.");
    }
  }

  class Program
  {
    static void Main(string[] args)
    {
      Student student = new Student();
      student.Name();
    }
  }
}
